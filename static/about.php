	<p>The Questing Clan of Runescape - or Clan Quest for short - is a clan representing every nationality known to play Runescape. All of us are united by our love of quests and lore. As a clan, we specialize and have a strong interest in completing every quest in game. Our members particularly enjoy helping others to achieve this goal through skilling and advice. We invite anyone, from the novice quester to the Quest Cape owner, to join our community.</p>

	<section>
		<h2>A brief history</h2>
		<p>Clan Quest was founded in November 2009 by a bunch of questers in Runescape. After inviting all the "Quest Noobs" they knew, they requested recognition from Jagex and were formally recognized in the Clan Database on December 8th, 2009. Hereafter, all new recruits are called noobs to this day.<p>

		<p>Clan Quest was featured in the Clan Blog (by Mod Timbo) August 4th, 2010, and featured again during the "Clan Celebration Month" of March, 2011. With the creation of our own website and continued recognition from Jagex and the game community, Clan Quest has steadily increased in popularity. As it stands now, Clan Quest has over 450 members and is the largest questing community in Runescape!</p>

		<p>In 2016 Clan Quest started a push to broaden its horizons. With the number of in-person meetings increasing and members looking for more ways to stay in touch with their friends outside of the game, it was clear we needed to accommodate for more. While Runescape is and will always be a central pillar in the clan, we are now expanding into a larger space and welcome everybody who wishes to be part of the Clan Quest family.</p>

		<p>Today, the clan has grown to something bigger than the shared love for a game. We are currently in the process of opening up the clan for people not playing Runescape as well.</p>
	</section>

	<section>
		<h2>Our core values</h2>
		<p>Clan Quest considers itself a family. The bonds created within this clan transcend gender and nationality. Clan Quest has a very strict joining process, but this means that once you are part of the clan, you are automatically part of the family. As soon as you have joined, you are eligible to join any event or communication channel we have set up for our clan.</p>

		<p>We take our relationship to our clan members very seriously, and we expect our members to show a minimal dedication to the clan as well. We believe strongly that real life commitments are more important than commitment to the clan, and we do not have activity requirements to retain membership to the clan. People offer a varying amount of time to keep the clan running, and we regularly hold in-person meetings to have some fun in real life.</p>

		<h3>"Noob"</h3>
		<p>Ever been called a noob? Fear not, for this is no bad thing! In our clan, it’s considered a term of endearment, with many of our members incorporating the word 'noob' into their nicknames. Everybody starts out inexperienced, and we encourage our members to learn and grow together. If you’re not an expert, you’re a noob. But you’re OUR noob! Since nobody is an expert in everything... we are all noobs!</p>

		<p>Rest assured, if you are called a noob you are considered a part of our clan family. We take this very seriously! Feel free to call any of us noobs (as long as you remain polite) and please mind our intentions if you are addressed as a noob.</p>

		<h3>Rules</h3>
		<ol>
			<li>We abide by the rules of the game we are playing.</li>
			<li>No insulting/offensive behavior (including spamming and excessive profanity).</li>
			<li>Conflict? Deal with it in private.</li>
			<li>Avoid sensitive topics as they can cause conflict, and be respectful of divergent behavior/viewpoints.</li>
			<li>All clan members must abide to our Code of Ethics.</li>
		</ol>
	</section>

